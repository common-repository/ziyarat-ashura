سلام بر تو اى ابا عبداللّه و بر روانهائى که فرود آمدند به آستانت ، بر تو از جانب من سلام خدا باد هميشه تا من زنده ام و برپا است شب و روز و قرار ندهد اين زيارت را خداوند آخرين بار زيارت من از شما
سلام بر حسين و بر على بن الحسين و بر فرزندان حسين و بر اصحاب و ياران حسين
]
</span>
</p>
<p>
<span class="za-span-red">سپس می گویی</span>
36-اَللّهُمَّ خُصَّ اَنْتَ اَوَّلَ ظالِمٍ بِاللَّعْنِ مِنّى وَ ابْدَاءْ بِهِ اَوَّلاً ثُمَّ الثّانِىَ وَالثّالِثَ وَ الرّابِعَ
<br>
37-اَللّهُمَّ الْعَنْ يَزيدَ خامِساً وَ الْعَنْ عُبَيْدَ اللَّهِ بْنَ زِيادٍ وَ ابْنَ مَرْجانَةَ وَ عُمَرَ بْنَ سَعْدٍ وَ شِمْراً وَ آلَ اَبى سُفْيانَ وَ آلَ زِيادٍ وَ آلَ مَرْوانَ اِلى يَوْمِ الْقِيمَةِ
<span class="za-span">
[
خدايا مخصوص گردان نخستين ستمگر را به لعنت من و آغاز کن بدان لعن اولى را و سپس دومى و سومى و چهارمى را
خدايا لعنت کن يزيد را در مرتبه پنجم و لعنت کن عبيداللّه پسر زياد و پسر مرجانه را و عمر بن سعد و شمر و دودمان ابوسفيان و دودمان زياد و دودمان مروان را تا روز قيامت
]
</span>
</p>
<p>
<span class="za-span-red">به سجده می روی و می گویی</span>
38-اَللّهُمَّ لَكَ الْحَمْدُ حَمْدَ الشّاکِرينَ لَكَ عَلى مُصابِهِمْ اَلْحَمْدُ لِلَّهِ عَلى عَظيمِ رَزِيَّتى
<br>
39-اَللّهُمَّ الرْزُقْنى شَفاعَةَ الْحُسَيْنِ يَوْمَ الْوُرُودِ وَ ثَبِّتْ لى قَدَمَ صِدْقٍ عِنْدَكَ مَعَ الْحُسَيْنِ وَ اَصْحابِ الْحُسَيْنِ الَّذينَ بَذَلُوا مُهَجَهُمْ دُونَ الْحُسَيْنِ عَلَيْهِ السَّلامُ
<span class="za-span">
