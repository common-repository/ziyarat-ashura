خدايا روزيم گردان شفاعت حسين عليه السلام را در روز ورود (به صحراى قيامت ) و ثابت بدار گام راستيم را در نزد خودت با حسين عليه السلام و ياران حسين آنانكه بى دريغ دادند جان خود را در راه حسين عليه السلام.
]
</span>
</p>
