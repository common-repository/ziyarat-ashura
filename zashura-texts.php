سلام بر تو اى فرزند فاطمه بانوى زنان جهانيان سلام بر تو اى که خدا خونخواهيش کند و فرزند چنين کسى و اى کشته اى که انتقام کشته گانت نگرفتى
]
</span>
</p>
<p>
3-اَلسَّلامُ عَلَيْكَ وَعَلَى الاَْرْواحِ الَّتى حَلَّتْ بِفِناَّئِكَ عَلَيْكُمْ مِنّى جَميعاً سَلامُ اللَّهِ اَبَداً ما بَقيتُ وَ بَقِىَ اللَّيْلُ وَالنَّهارُ
<span class="za-span">
[
سلام بر تو و بر روانهائى که فرود آمدند به آستانت ، بر شما همگى از جانب من سلام خدا باد هميشه تا من برجايم و برجا است شب و روز
]
</span>
</p>
<p>
4-يا اَباعَبْدِاللَّهِ لَقَدْ عَظُمَتِ الرَّزِيَّةُ وَ جَلَّتْ وَ عَظُمَتِ الْمُصيبَةُ بِكَ عَلَيْنا وَ عَلى جَميعِ اَهْل ِالاِْسْلامِ
<span class="za-span">
[
اى ابا عبداللّه براستى بزرگ شد سوگوارى تو و گران و عظيم گشت مصيبت تو بر ما و بر همه اهل اسلام
]
</span>
</p>
<p>
5-و َجَلَّتْ وَ عَظُمَتْ مُصيبَتُكَ فِى السَّمواتِ عَلى جَميعِ اَهْلِ السَّمواتِ فَلَعَنَ اللَّهُ اُمَّةً اَسَّسَتْ اَساسَ الظُّلْمِ وَ الْجَوْرِ عَلَيْكُمْ اَهْلَ الْبَيْتِ
<span class="za-span">
[
و گران و عظيم گشت مصيبت تو در آسمانها بر همه اهل آسمانها پس خدا لعنت کند مردمى را که ريختند شالوده ستم و بيدادگرى را بر شما خاندان
]
</span>
</p>
<p>
6-وَ لَعَنَ اللَّهُ اُمَّةً دَفَعَتْكُمْ عَنْ مَقامِكُمْ و َاَزالَتْكُمْ عَنْ مَراتِبِكُمُ الَّتى رَتَّبَكُمُ اللَّهُ فيها
<span class="za-span">
[
و خدا لعنت کند مردمى را که کنار زدند شما را از مقام مخصوصتان و دور کردند شما را از آن مرتبه هائى که خداوند آن رتبه ها را به شما داده بود
]
</span>
</p>
<p>
7-و َلَعَنَ اللَّهُ اُمَّةً قَتَلَتْكُمْ وَ لَعَنَ اللَّهُ الْمُمَهِّدينَ لَهُمْ بِالتَّمْكينِ مِنْ قِتالِكُمْ
<span class="za-span">
[
و خدا لعنت کند مردمى که شما را کشتند و خدا لعنت کند آنانكه تهيه اسباب کردند براى کشندگان شما تا آنها توانستند با شما بجنگند
]
</span>
</p>
<p>
8-بَرِئْتُ اِلَى اللَّهِ وَ اِلَيْكُمْ مِنْهُمْ وَ مِنْ اَشْياعِهِمْ وَ اَتْباعِهِمْ وَ اَوْلِياَّئِهِم
<span class="za-span">
[
بيزارى جويم بسوى خدا و بسوى شما از ايشان و از پيروان و دنبال روندگانشان و دوستانشان
]
</span>
</p>
<p>
9-يا اَباعَبْدِاللَّهِ اِنّى سِلْمٌ لِمَنْ سالَمَكُمْ وَ حَرْبٌ لِمَنْ حارَبَكُمْ اِلى يَوْمِ الْقِيامَةِ
<span class="za-span">
[
اى اباعبداللّه من تسليمم و در صلحم با کسى که با شما در صلح است و در جنگم با هر کس که با شما در جنگ است تا روز قيامت
]
</span>
</p>
<p>
10-وَ لَعَنَ اللَّهُ آلَ زِيادٍ وَ آلَ مَرْوانَ وَ لَعَنَ اللَّهُ بَنى اُمَيَّةَ قاطِبَةً وَ لَعَنَ اللَّهُ ابْنَ مَرْجانَةَ
<span class="za-span">
[
و خدا لعنت کند خاندان زياد و خاندان مروان را و خدا لعنت کند بنى اميه را همگى و خدا لعنت کند فرزند مرجانه (ابن زياد) را
]
</span>
</p>
<p>
11-وَ لَعَنَ اللَّهُ عُمَرَ بْنَ سَعْدٍ وَ لَعَنَ اللَّهُ شِمْراً وَ لَعَنَ اللَّهُ اُمَّةً اَسْرَجَتْ وَ اَلْجَمَتْ وَ تَنَقَّبَتْ لِقِتالِكَ
<span class="za-span">
[
خدا لعنت کند عمر بن سعد را و خدا لعنت کند شمر را و خدا لعنت کند مردمى را که اسبها را زين کردند و دهنه زدند و به راه افتادند براى پيكار با تو
]
</span>
</p>
<p>
12-بِاَبى اَنْتَ وَ اُمّى لَقَدْ عَظُمَ مُصابى بِكَ فَاَسْئَلُ اللَّهَ الَّذى اَکْرَمَ مَقامَكَ
<span class="za-span">
[
پدر و مادرم بفدايت که براستى بزرگ شد مصيبت تو بر من پس مى خواهم از آن خدائى که گرامى داشت مقام تو را
]
</span>
</p>
<p>
13-وَ اَکْرَمَنى بِكَ اَنْ يَرْزُقَنى طَلَبَ ثارِكَ مَعَ اِمامٍ مَنْصُورٍ مِنْ اَهْلِ بَيْتِ مُحَمَّدٍ صَلَّى اللَّهُ عَلَيْهِ و َآلِهِ
<span class="za-span">
[
و گرامى داشت مرا بخاطر تو که روزيم گرداند خونخواهى تو را در رکاب آن امام يارى شده از خاندان محمد صلى اللّه عليه و آله
]
</span>
</p>
<p>
14-اَللّهُمَّ اجْعَلْنى عِنْدَكَ وَجيهاً بِالْحُسَيْنِ عَلَيْهِ السَّلامُ فِى الدُّنْيا وَ الاْخِرَةِ
<span class="za-span">
[
خدايا قرار ده مرا نزد خودت آبرومند بوسيله حسين عليه السلام در دنيا و آخرت
]
</span>
</p>
<p>
15-يا اَباعَبْدِاللَّهِ اِنّى اَتَقَرَّبُ اِلى اللَّهِ وَ اِلى رَسُولِهِ وَ اِلى اميرِالْمُؤْمِنينَ وَ اِلى فاطِمَةَ وَ اِلَى الْحَسَنِ
<span class="za-span">
[
اى اباعبداللّه من تقرب جويم به درگاه خدا و پيشگاه رسولش و اميرالمؤمنين و فاطمه و حسن
]
</span>
</p>
<p>
16-وَ اِلَيْكَ بِمُوالاتِكَ وَ بِالْبَراَّئَةِ (مِمَّنْ قاتَلَكَ وَ نَصَبَ لَكَ الْحَرْبَ وَ بِالْبَرائَةِ مِمَّنْ اَسَّسَ اَساسَ الظُّلْمِ وَ الْجَوْرِعَلَيْكُمْ وَ اَبْرَءُ اِلَى اللّهِ وَ اِلى رَسُولِهِ) مِمَّنْ اَسَسَّ اَساسَ ذلِكَ وَ بَنى عَلَيْهِ بُنْيانَهُ وَ جَرى فى ظُلْمِهِ وَ جَوْرِهِ عَلَيْكُمْ وَ على اَشْياعِكُمْ
<span class="za-span">
[
و شما بوسيله دوستى تو و بوسيله بيزارى از کسى که با تو مقاتله کرد و جنگ با تو را برپا آرد و به بيزارى جستن از کسى که شالوده ستم و ظلم بر شما را ريخت و بيزارى جويم بسوى خدا و بسوى رسولش از کسى که پى ريزى کرد شالوده اين کار را و پايه گذارى کرد بر آن بنيانش را و دنبال کرد ستم و ظلمش را بر شما و بر پيروان شما
]
</span>
</p>
<p>
17-بَرِئْتُ اِلَى اللَّهِ وَ اِلَيْكُمْ مِنْهُمْ وَ اَتَقَرَّبُ اِلَى اللَّهِ ثُمَّ اِلَيْكُمْ بِمُوالاتِكُمْ وَ مُوالاةِ وَلِيِّكُمْ
<span class="za-span">
[
بيزارى جويم بدرگاه خدا و به پيشگاه شما از ايشان و تقرب جويم بسوى خدا سپس بشما بوسيله دوستيتان و دوستى دوستان شما
]
</span>
</p>
<p>
18-وَ بِالْبَرآئَةِ مِنْ اَعْداَّئِكُمْ وَ النّاصِبينَ لَكُمُ الْحَرْبَ وَ بِالْبَرآئَةِ مِنْ اَشْياعِهِمْ وَ اَتْباعِهِمْ
<span class="za-span">
[
و به بيزارى از دشمنانتان و برپا کنندگان (و آتش افروزان ) جنگ با شما و به بيزارى از ياران و پيروانشان
]
</span>
</p>
<p>
19-اِنّى سِلْمٌ لِمَنْ سالَمَكُمْ وَحَرْبٌ لِمَنْ حارَبَكُمْ وَ وَلِىُّ لِمَنْ والاکُمْ وَ عَدُوُّ لِمَنْ عاداکُمْ
<span class="za-span">
[
من در صلح و سازشم با کسى که با شما در صلح است و در جنگم با کسى که با شما در جنگ است و دوستم با کسى که شما را دوست دارد و دشمنم با کسى که شما را دشمن دارد
]
</span>
</p>
<p>
20-فَاَسْئَلُ اللَّهَ الَّذى اَکْرَمَنى بِمَعْرِفَتِكُمْ وَمَعْرِفَةِ اَوْلِياَّئِكُمْ وَ رَزَقَنِى الْبَراَّئَةَ مِنْ اَعْداَّئِكُمْ
<span class="za-span">
[
و درخواست کنم از خدائى که مرا گرامى داشت بوسيله معرفت شما و معرفت دوستانتان و روزيم کند بيزارى جستن از دشمنانتان را
]
</span>
</p>
<p>
21-اَنْ يَجْعَلَنى مَعَكُمْ فِى الدُّنْيا وَالاْخِرَةِ وَاَنْ يُثَبِّتَ لى عِنْدَکُمْ قَدَمَ صِدْقٍ فِى الدُّنْيا وَ الاْخِرَةِ
<span class="za-span">
[
به اينكه قرار دهد مرا با شما در دنيا و آخرت و پابرجا دارد براى من در پيش شما گام راست و درستى (و ثبات قدمى) در دنيا و آخرت
]
</span>
</p>
<p>
22-وَ اَسْئَلُهُ اَنْ يُبَلِّغَنِى الْمَقامَ الْمَحْمُودَ لَكُمْ عِنْدَ اللَّهِ وَ اَنْ يَرْزُقَنى طَلَبَ ثارى مَعَ اِمامٍ هُدىً ظاهِرٍ ناطِقٍ (بِالْحَقِّ) مِنْكُمْ
<span class="za-span">
[
و از او خواهم که برساند مرا به مقام پسنديده شما در پيش خدا و روزيم کند خونخواهى شما را با امام راهنماى آشكار گوياى (به حق) که از شما (خاندان ) است
]
</span>
</p>
<p>
23-وَ اَسْئَلُ اللَّهَ بِحَقِّكُمْ وَبِالشَّاْنِ الَّذى لَكُمْ عِنْدَهُ اَنْ يُعْطِيَنى بِمُصابى بِكُمْ اَفْضَلَ ما يُعْطى مُصاباً بِمُصيبَتِهِ مُصيبَةً ما اَعْظَمَه
<span class="za-span">
[
و از خدا خواهم به حق شما و بدان منزلتى که شما نزد او داريد ، که عطا کند به من بوسيله مصيبتى که از ناحيه شما به من رسيده بهترين پاداشى را که مى دهد به يك مصيبت زده از مصيبتى که ديده
]
</span>
</p>
<p>
24-وَ اَعْظَمَ رَزِيَّتَها فِى الاِْسْلامِ وَ فى جَميعِ السَّمواتِ وَ الاَْرْضِ اَللّهُمَّ اجْعَلْنى فى مَقامى هذا مِمَّنْ تَنالُهُ مِنْكَ صَلَواتٌ وَ رَحْمَةٌ وَ مَغْفِرَةٌ
<span class="za-span">
[
براستى چه مصيبت بزرگى و چه داغ گرانى بود در اسلام و در تمام آسمانها و زمين خدايا چنانم کن در اينجا که ايستاده ام از کسانى باشم که برسد بدو از ناحيه تو درود و رحمت و آمرزشى
]
</span>
</p>
<p>
25-اَللّهُمَّ اجْعَلْ مَحْياىَ مَحْيا مُحَمَّدٍ وَ آلِ مُحَمَّدٍ وَ مَماتى مَماتَ مُحَمَّدٍ وَ آلِ مُحَمَّدٍ
<span class="za-span">
[
خدايا قرار ده زندگيم را زندگى محمد و آل محمد و مرگم را مرگ محمد و آل محمد
]
</span>
</p>
<p>
26-اَللّهُمَّ اِنَّ هذا يَوْمٌ تَبرَّکَتْ بِهِ بَنُو اُمَيَّةَ وَ ابْنُ آکِلَةِ الَْآکبادِ اللَّعينُ ابْنُ اللَّعينِ عَلى لِسانِكَ وَ لِسانِ نَبِيِّكَ صَلَّى اللَّهُ عَلَيْهِ وَ آلِهِ
<span class="za-span">
[
خدايا اين روز روزى است که مبارك و ميمون دانستند آنرا بنى اميه و پسر آن زن جگرخوار (معاويه ) آن ملعون پسر ملعون (آه لعن شده ) بر زبان تو و زبان پيامبرت که درود خدا بر او و آلش باد
]
</span>
</p>
<p>
27-فى کُلِّ مَوْطِنٍ وَ مَوْقِفٍ وَقَفَ فيهِ نَبِيُّكَ صَلَّى اللَّهُ عَلَيْهِ وَ آلِهِ
<span class="za-span">
[
در هر جا و هر مكانى که توقف کرد در آن مكان پيامبرت صلى اللّه عليه و آله
]
</span>
</p>
<p>
28-اَللّهُمَّ الْعَنْ اَباسُفْيانَ وَ مُعاوِيَةَ وَ يَزيدَ بْنَ مُعاوِيَةَ عَلَيْهِمْ مِنْكَ اللَّعْنَةُ اَبَدَ الاْبِدينَ
<span class="za-span">
[
خدايا لعنت کن ابوسفيان و معاويه و يزيد بن معاويه را که لعنت بر ايشان باد از جانب تو براى هميشه
]
</span>
</p>
<p>
29-وَ هذا يَوْمٌ فَرِحَتْ بِهِ آلُ زِيادٍ وَ آلُ مَرْوانَ بِقَتْلِهِمُ الْحُسَيْنَ صَلَواتُ اللَّهِ عَلَيْهِ
<span class="za-span">
[
و اين روز روزى است که شادمان شدند به اين روز دودمان زياد و دودمان مروان بخاطر کشتنشان حضرت حسين صلوات اللّه عليه را
]
</span>
</p>
<p>
30-اَللّهُمَّ فَضاعِفْ عَلَيْهِمُ اللَّعْنَ مِنْكَ وَ الْعَذابَ (الاَْليمَ) اَللّهُمَّ اِنّى اَتَقَرَّبُ اِلَيْكَ فى هذَا الْيَوْمِ وَ فى مَوْقِفى هذا
<span class="za-span">
[
خدايا پس چندين برابر کن بر آنها لعنت خود و عذاب دردناك را خدايا من تقرب جويم بسوى تو در اين روز و در اين جائى که هستم
]
</span>
</p>
<p>
31-وَ اَيّامِ حَياتى بِالْبَراَّئَهِ مِنْهُمْ وَاللَّعْنَةِ عَلَيْهِمْ وَ بِالْمُوالاتِ لِنَبِيِّكَ وَ آلِ نَبِيِّكَ عَلَيْهِ وَ عَلَيْهِمُ اَلسَّلامُ
<span class="za-span">
[
و در تمام دوران زندگيم به بيزارى جستن از اينها و لعنت فرستادن بر ايشان و بوسيله دوست داشتن پيامبرت و خاندان پيامبرت که بر او و بر ايشان سلام باد
]
</span>
</p>
<p>
<span class="za-span-red">می گویی صد مرتبه</span>
32-اَللّهُمَّ الْعَنْ اَوَّلَ ظالِمٍ ظَلَمَ حَقَّ مُحَمَّدٍ وَ آلِ مُحَمَّدٍ وَ آخِرَ تابِعٍ لَهُ عَلى ذلِكَ
<br>
33-اَللّهُمَّ الْعَنِ الْعِصابَةَ الَّتى جاهَدَتِ الْحُسَيْنَ وَ شايَعَتْ وَ بايَعَتْ وَ تابَعَتْ عَلى قَتْلِهِ اَللّهُمَّ الْعَنْهُمْ جَميعاً
<span class="za-span">
[
خدايا لعنت کن نخستين ستمگرى را که بزور گرفت حق محمد و آل محمد را و آخرين کسى که او را در اين زور و ستم پيروى کرد
خدايا لعنت کن بر گروهى که پيكار کردند با حسين عليه السلام و همراهى کردند و پيمان بستند و از هم پيروى کردند براى کشتن آن حضرت خدايا لعنت کن همه آنها را
]
</span>
</p>
<p>
<span class="za-span-red">می گویی صد مرتبه</span>
34-اَلسَّلامُ عَلَيْكَ يا اَباعَبْدِاللَّهِ وَ عَلَى الاَْرْواحِ الَّتى حَلَّتْ بِفِناَّئِكَ عَلَيْكَ مِنّى سَلامُ اللَّهِ (اَبَداً) ما بَقيتُ وَ بَقِىَ اللَّيْلُ وَ النَّهارُ وَ لاجَعَلَهُ اللَّهُ آخِرَ الْعَهْدِ مِنّى لِزِيارَتِكُمْ
<br>
35-اَلسَّلامُ عَلَى الْحُسَيْنِ وَ عَلى عَلِىِّ بْنِ الْحُسَيْنِ وَ عَلى اَوْلادِ الْحُسَيْنِ وَ عَلى اَصْحابِ الْحُسَيْنِ
<span class="za-span">
